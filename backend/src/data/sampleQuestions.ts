// Helper function to get questions by age group
export const getQuestionsByAge = (ageGroup: '5-10' | '10-15' | '15-18'): Question[] => {
  return sampleQuestions.filter(q => q.ageGroup === ageGroup);
};

// Helper function to get questions by difficulty
export const getQuestionsByDifficulty = (difficulty: 'easy' | 'medium' | 'hard'): Question[] => {
  return sampleQuestions.filter(q => q.difficulty === difficulty);
};

// Helper function to get random questions
export const getRandomQuestions = (
  count: number = 25,
  ageGroup?: '5-10' | '10-15' | '15-18',
  difficulty?: 'easy' | 'medium' | 'hard'
): Question[] => {
  let filtered = sampleQuestions;
  
  if (ageGroup) {
    filtered = filtered.filter(q => q.ageGroup === ageGroup);
  }
  
  if (difficulty) {
    filtered = filtered.filter(q => q.difficulty === difficulty);
  }
  
  // Shuffle and return requested count
  const shuffled = filtered.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
};